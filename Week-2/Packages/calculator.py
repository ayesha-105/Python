def add(a, b):
    return a + b

def subtract (a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    if b == 0:
        print(" This number is not divisble by zero")
    else:
        return a / b
    
def power(a, b):
    return a ** b

def modulus(a, b):
    return a % b

